fn custom_criterion() -> Criterion {
    Criterion::default()
        .output_directory(std::path::Path::new("./report"))
        .sample_size(100)
        .measurement_time(std::time::Duration::from_secs(7))
}

criterion_group! {
    name = benches;
    config = custom_criterion();
    targets = all_benches
}
criterion_main!(benches);
